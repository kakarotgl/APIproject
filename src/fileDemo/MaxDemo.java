package fileDemo;

public class MaxDemo {

	public static void main(String[] args) {
		int max=Integer.MAX_VALUE;
		int min=Integer.MIN_VALUE;
		System.out.println(max);
		System.out.println(min);
		
		long lmx=Long.MAX_VALUE;
		long lmi=Long.MIN_VALUE;
		System.out.println(lmx);
		System.out.println(lmi);
		
		short smx=Short.MAX_VALUE;
		short smi=Short.MIN_VALUE;
		System.out.println(smx);
		System.out.println(smi);
		
		byte bmx=Byte.MAX_VALUE;
		byte bmi=Byte.MIN_VALUE;
		System.out.println(bmx);
		System.out.println(bmi);
		
		float fmx=Float.MAX_VALUE;
		float fmi=Float.MIN_VALUE;
		System.out.println(fmx);
		System.out.println(fmi);

	}

}
