package refDemo;

public class BigAirplane {
	int x;
	int y;
	int width;
	int height;
	
	public BigAirplane(int x,int y,int width,int height) {
		this.x=x;
		this.y=y;
		this.width=width;
		this.height=height;
	}
	public String toString() {
		return "x="+x+",y="+y+",width="+width+",height="+height;
	}

}
